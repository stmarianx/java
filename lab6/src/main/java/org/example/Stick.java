package org.example;

public class Stick {
    // Sticks are represented by their two endpoints (x1,y1) and (x2,y2)
    final int x1, y1, x2, y2;

    public Stick(int x1, int y1, int x2, int y2) {
        this.x1 = x1;
        this.y1 = y1;
        this.x2 = x2;
        this.y2 = y2;
    }
}
